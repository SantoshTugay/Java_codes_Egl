package interview_prgrams.GforG;

public class swapNumbers {
    public static void main(String[] args) {
        int a=1;
        int b=2;
        int tmp;
        System.out.println("Before Swaping : "+ a +" "+b);
        tmp=a;
        a=b;
        b=tmp;

        System.out.println("After Swaping : "+ a +" "+b);
    }
}
